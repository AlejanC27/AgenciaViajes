    private void btnAgregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarClienteActionPerformed
        // TODO add your handling code here:
        String [] dates = new String [6];
        dates [0] = txtIdCliente.getText();
        dates [1] = txtNombreCliente.getText();
        dates [2] = txtApellidoCliente.getText();
        dates [3] = txtGeneroCliente.getText();
        dates [4] = txtEdadCliente.getText();
        dates [5] = txtContactoCliente.getText();
        modelo.addRow(dates);
        txtIdCliente.setText("");
        txtNombreCliente.setText("");
        txtApellidoCliente.setText("");
        txtGeneroCliente.setText("");
        txtEdadCliente.setText("");
        txtContactoCliente.setText("");
    }//GEN-LAST:event_btnAgregarClienteActionPerformed

    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        // TODO add your handling code here:
        int fila = tablaCliente.getSelectedRow();
        if (fila >= 0 ){
            modelo.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(null,"Seleccionar Fila");
        }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

    private void btnEliminarTdoClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTdoClienteActionPerformed
        // TODO add your handling code here:
        int fila = tablaCliente.getRowCount();
        for (int i = fila -1;i>=0;i--){
            modelo.removeRow(i);
        }
    }//GEN-LAST:event_btnEliminarTdoClienteActionPerformed

    private void btnModificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarClienteActionPerformed
        // TODO add your handling code here:
        int fila = Integer.parseInt(txtFilaCliente.getText());
        int columna = Integer.parseInt(txtColumnaCliente.getText());
        modelo.setValueAt(txtNuevoDatoCliente.getText(),fila,columna);
        //PARA QUE FUNCIONE, SE CUENTA DESDE 0 
        //COLUMNA ID = 0, NOMBRE = 1, Y ASI SUCESIVAMENTE
    }//GEN-LAST:event_btnModificarClienteActionPerformed

